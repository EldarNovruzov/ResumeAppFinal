    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        int selectedRow = tblSkill.getSelectedRow();
        System.out.println(selectedRow);
        UserSkill userskill = list.get(selectedRow);
        System.out.println(userskill);
        userskills.removeUserSkill(userskill.getId());
        fillWindow();
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        String skillname = txtSkillName.getText();
        Skill skill = null;
        if (skillname != null && !skillname.trim().isEmpty()) {
            skill = new Skill(0, skillname);
            skills.insertSkill(skill);
        } else {
            skill = (Skill) cbSkillName.getSelectedItem();
        }

        int power = sliderPower.getValue();
        UserSkill ss = new UserSkill(null, Config.loggedInUser, skill, power);
        userskills.insertUserSkill(ss);
        fillWindow();
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        UserSkill get = list.get(tblSkill.getSelectedRow());
        System.out.println("----------------"
                + get
                +"\n-------------------------");
        String skillname = txtSkillName.getText();
        Skill skill = null;
        if (skillname != null && !skillname.trim().isEmpty()) {
            skill = new Skill(0, skillname);
            skills.insertSkill(skill);
        } else {
            skill = (Skill) cbSkillName.getSelectedItem();
        }

        int power = sliderPower.getValue();

        get.setSkill(skill);
        get.setPower(power);

        userskills.updateUserSkill(get);
        fillTable();
    }//GEN-LAST:event_btnSaveActionPerformed


