    public void fillWindow() {
        List<Skill> allSkills = skills.getallSkills();
        for (Skill c : allSkills) {
            cbSkillName.addItem(c);
        }
        fillTable();
    }
    private List<UserSkill> list;

    public void fillTable() {

        list = userskills.getAllSkillByUserId(Config.loggedInUser.getId());

        Vector<Vector> rows = new Vector<>();
        for (UserSkill l : list) {
            Vector row = new Vector();
            row.add(l.getSkill());
            row.add(l.getPower());
            rows.add(row);
        }
        DefaultTableModel model = new DefaultTableModel(rows, new Vector(Arrays.asList("Skill", "Power")));
        tblSkill.setModel(model);

    }

    public void fillUserComponents() {
        fillWindow();

    }

